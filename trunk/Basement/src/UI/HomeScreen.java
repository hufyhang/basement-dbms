    private void switchContentPanel( int index ) {
        UI.Panel.Panel_Template taskPanel = null;
        switch (index) {
            case 0:
                taskPanel = new UI.Panel.Tables_Home_Panel( this.current_database );
                break;

            case 1:
                taskPanel = new UI.Panel.Queries_Home_Panel( this.current_database );
                break;

            case 2:
                taskPanel = new UI.Panel.Views_Home_Panel( this.current_database );
                break;

            default:
                break;
        }
        taskPanel.setVisible( true );
        this.jSplitPane1.setRightComponent( taskPanel );
        this.current_panel = taskPanel;
        this.server.setCurrentPanel( this.current_panel );
    }

