    public HomeScreen( String LookAndFeel, String DBContainer, int port, boolean offline ) {
        this.current_database = DBContainer;
        this.server = new BSQL.Server_Java( this, this.current_database, port ); // initialize the daemon thread for server

        this.setDefaultCloseOperation( javax.swing.JFrame.EXIT_ON_CLOSE );
        UI.Theme.Themes.setLookAndFeel( LookAndFeel );
        initComponents();
        this.dbContainer = DBContainer;
        this.initialDatabase( this.dbContainer );
        this.switchContentPanel( TABLES_PANEL );
        
        String portNumber = "Offline";
        if( !offline ) {
            portNumber = String.valueOf( port );
            this.server.setCurrentPanel( this.current_panel );
            this.server.setDaemon( true );
            this.server.start();
        }
        this.setTitle( this.dbContainer + ":" + portNumber + " - Basement" );
    }

