    public void initialDatabase( String DBContainer ) {
        this.ComboBox_Database.removeAllItems();
        ArrayList list = Ctrl.InitialDatabase.getDatabase( DBContainer );
        for( int index = 0; index != list.size(); ++index ) {
            this.ComboBox_Database.addItem( list.get( index ) );
        }
    }

