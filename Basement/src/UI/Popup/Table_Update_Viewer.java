    private void Button_GrabActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_Button_GrabActionPerformed
        String line = null;
        switch ( this.ComboBox_Mode.getSelectedIndex() ) {
            case 0:
                line = "INSERT " + this.table + " :\n";
                break;

            case 1:
                line = "REMOVE " + this.table + " :\n";
                break;

            case 2:
                line = "UPDATE " + this.table + ".Field_Name TO VALUE :\n";
                break;

            default:
                break;
        }
        this.TextArea_BSQL.setCaretPosition( 0 );
        this.TextArea_BSQL.replaceSelection( line );
    }//GEN-LAST:event_Button_GrabActionPerformed

