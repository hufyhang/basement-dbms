    protected void BSQL_Fire() {
        if( BSQL.BSQL_Core.BSQL_Expresion(  this.current_database, this.TextArea_BSQL.getText() ) ) {
            this.panel.setTable_Document();
            this.Table_Document.setModel( this.panel.getTable_Document().getModel() );
        }
        else {
            new javax.swing.JOptionPane().showMessageDialog( this, "Unable to operate record.", "Error", 0 );
        }
    }
